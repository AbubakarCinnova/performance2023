<h1>What is a ductless mini-split system? </h1>
 <p>Ductless mini split heating and cooling systems are high efficient heating and cooling systems that deliver warm or cool conditioned air directly into different rooms in your home, without the use of ducts. You may also hear them called mini-split and multi-split heat pumps or ductless mini split air conditioning systems.</p>
 
 
  <br/>
 <p class="text-center"><img src="<?php echo $base_url ?>/img/lennox-mini-split.jpg" alt="image" class="img-responsive"></p>
<br/> 
 <p>Ductless mini split heating and cooling units use small heating and cooling air handlers for every individual room, so installing a ductless mini-split system makes it easy to set up heating and cooling zones in your home. Which means each room in your home can be at a different temperature and comfort level.</p>
 <p>Some of our mini split ductless systems can accommodate up to five indoor units that can provide heating or cooling, and each of these air handlers can be controlled individually via its own wireless thermostat.</p>
 <p>Since mini splits have no need of ducts, they avoid the energy losses associated with ductwork of central air systems. Heated or cooled air lost through leaky or uninsulated  ducts in your home, can account for more than 30% of energy consumption for space conditioning, especially if the ducts are in an unconditioned space such as an attic. So this makes a ductless mini-split highly energy efficient.</p>
 <h2>Popular uses of ductless mini split systems include:</h2>
 <p> <strong>Older Homes -</strong> Great heating and cooling solution for older homes that were not constructed with a ductwork system in place. Example a older home with a boiler style heating system has no ducting.</p>
 <p> <strong>Sun Rooms or Enclosed Patios -</strong> Ductless mini splits can provide efficient year-round comfort with heating and cooling. </p>
 <p> <strong>Outbuildings - </strong>Ductless mini and multi splits are great for guest houses and workshops providing both heating and cooling. </p>
 <p> <strong>New Additions and Remodels - </strong>Ductless Mini splits are the perfect heating and cooling solution for remodels, or new additions to the home. </p>
 <p> <strong>Home Offices - </strong>A ductless mini split heating and cooling system will make your home office comfortable and help you be more productive year round.</p>
 <p> <strong>Man Caves - </strong>With so many heat producing electronics, a ductless mini split is the perfect solution to keep your man cave comfortable all year long.
</p>
 <p> <strong>Home Theater Rooms - </strong>Entertain in comfort.    </p>
 <p> <strong>Garages - </strong>Work on your car in the middle of Winter or the hottest day of the Summer and be truly comfortable. </p>
