<h2>How It Works</h2>
<p>iWave is a whole-home air purifying device that installs inside of any ducted HVAC central air system.  Its patented technology, called needle-point bi-polar ionization, injects equal amounts of positive and negative ions into the air stream.  These ions break down passing pollutants and gases into harmless compounds like oxygen, carbon dioxide, nitrogen and water vapor.</p>

<p>When the ions make contact with active viruses, bacteria or mold spores, they remove the hydrogen molecules.  Without hydrogen, these harmful pathogens have no source of energy and will die off.  The ions also attach themselves to allergens such as pollen, pet dander, dust and other particles, causing them to band together until they are large enough to be caught in your home’s air filter.</p>

